    protected void TableGolonganGaji() {

        Object[] Baris = {"ID", "Jenis", "Gaji Karyawan"};
        tableExGolongan = new DefaultTableModel(null, Baris);

        try {
            String sql = "SELECT * FROM golongan order by id asc";
            Statement stat = Models.Config.Conn().createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tableExGolongan.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3)
                });
            }
            golongan_ex_tabel.setModel(tableExGolongan);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal Pelamar: " + e);
        }
    }

