    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        if (ex_id_kar.getText().isEmpty() || ex_alamat_kar.getText().isEmpty() || ex_dept_kar.getText().isEmpty()
                || ex_jabatan_kar.getText().isEmpty() || ex_nama_kar.getText().isEmpty() || ex_pt_kar.getSelectedItem().equals("Pilih")
                || ex_sdk_posisi.getSelectedItem().equals("Pilih") || ex_status_kar.getSelectedItem().equals("Pilih") ||
                ex_telp_kar.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Belum Terpenuhi!", "Perhatian!", HEIGHT, warning);
        } else {
            try {
                String sql = "update karyawan set department=?,jabatan=?,posisi=?,nama_pt=?,status=?, status_data=? where id='" + ex_id_kar.getText() + "'";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, ex_dept_kar.getText());
                stat.setString(2, ex_jabatan_kar.getText());
                stat.setString(3, ex_sdk_posisi.getSelectedItem().toString());
                stat.setString(4, ex_pt_kar.getSelectedItem().toString());
                stat.setString(5, ex_status_kar.getSelectedItem().toString());
                stat.setString(6, ex_showkaryawan_text.getText());

                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Di Ubah!", "Berhasil!", HEIGHT, sucess);
                dispose();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Terjadi Kesalahan!", "Perhatian!", HEIGHT, invalid);
            }
        }
    }//GEN-LAST:event_jPanel2MouseClicked

