    protected void getCBposisi(){
        String sql = "SELECT * FROM client";      
        try {
            ps = Models.Config.Conn().prepareStatement(sql);
            rs=ps.executeQuery();
            while (rs.next()) {                
                String Posisi = rs.getString("nama");
                ex_pt_kar.addItem(Posisi);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Memuat"+e.getMessage(), "Peringatan", HEIGHT);
        }
    }
    
    protected void getCBclient(){
        String sql = "SELECT * FROM posisi";      
        try {
            ps = Models.Config.Conn().prepareStatement(sql);
            rs=ps.executeQuery();
            while (rs.next()) {                
                String Posisi = rs.getString("posisi");
                ex_sdk_posisi.addItem(Posisi);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Memuat"+e.getMessage(), "Peringatan", HEIGHT);
        }
    }
    
